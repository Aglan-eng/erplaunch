function xmlEscape(s: string): string {
  return s
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function buildCurrencyXml(args: {
  scriptid: string;
  iso: string;
  name: string;
  symbol: string;
}): string {
  const escapedName = xmlEscape(args.name);
  const escapedSymbol = xmlEscape(args.symbol);
  return `<?xml version="1.0" encoding="UTF-8"?>
<!--
  Generated by ERPLaunch Currency Generator. ISO 4217: ${args.iso}.
  Verify exchange-rate provider + base-currency designation in NetSuite UI before deploy.
-->
<currency scriptid="${args.scriptid}">
  <name>${escapedName}</name>
  <symbol>${escapedSymbol}</symbol>
  <isbasecurrency>F</isbasecurrency>
  <isinactive>F</isinactive>
  <displaysymbol>${escapedSymbol}</displaysymbol>
  <symbolplacement>_BEFORENUMBER</symbolplacement>
  <currencyprecision>2</currencyprecision>
</currency>
`;
}

/**
 * Emit one currency XML per ISO code. Empty input → empty output.
 * Deduplication + uppercasing applied defensively.
 */
export function generateCurrencies(input: CurrencyGeneratorInput): CurrencyGeneratorOutput {
  const files: Record<string, string> = {};
  const emitted: EmittedCurrency[] = [];

  const seen = new Set<string>();
  for (const codeRaw of input.currencies) {
    const iso = (codeRaw ?? '').trim().toUpperCase();
    if (!/^[A-Z]{3}$/.test(iso)) continue; // drop garbage
    if (seen.has(iso)) continue;
    seen.add(iso);

    const meta = CURRENCY_META[iso] ?? { name: iso, symbol: iso };
    const scriptid = `currency_${iso.toLowerCase()}`;
    const filename = `Objects/${scriptid}.xml`;
    files[filename] = buildCurrencyXml({
      scriptid,
      iso,
      name: meta.name,
      symbol: meta.symbol,
    });
    emitted.push({ filename, scriptid, iso, name: meta.name, symbol: meta.symbol });
  }

  return { files, emitted };
}
