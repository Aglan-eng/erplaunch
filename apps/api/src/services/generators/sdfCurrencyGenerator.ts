/**
 * SDF Currency generator (Pack A — OneWorld Foundation).
 *
 * Emits one Oracle SDF `currency` XML per ISO 4217 code that appears
 * in the engagement's subsidiary list. Without these files the
 * subsidiaries reference currency codes that don't exist on the
 * tenant — SDF deploy fails immediately.
 *
 * Caller passes a deduplicated array of ISO 4217 codes (extract via
 * `extractCurrenciesFromSubsidiaries` from the subsidiary generator).
 * Each code → one XML with a name + symbol from a built-in lookup
 * table. Unknown codes get name = symbol = the code itself (the
 * consultant fixes manually post-generation).
 *
 * Sources:
 *   - NetSuite SDF currency XML reference (Oracle docs).
 *   - ISO 4217 currency code list.
 */

export interface CurrencyGeneratorInput {
  /** Array of uppercased ISO 4217 codes. Duplicates are tolerated
   *  (deduped internally) but the orchestrator should already have
   *  deduplicated before passing in. */
  currencies: string[];
}

export interface EmittedCurrency {
  filename: string;
  scriptid: string;
  iso: string;
  name: string;
  symbol: string;
}

export interface CurrencyGeneratorOutput {
  files: Record<string, string>;
  emitted: EmittedCurrency[];
}

interface CurrencyMeta {
  name: string;
  symbol: string;
}

